class Solution:
    def numDecodings(self, s: str) -> int:
        n=len(s)
        # dp={n:1}
        dp2=0
        dp1=1
        dp=0
        for i in range(n-1,-1,-1):
            if s[i]=='0':
                dp=0
            else:
                dp=dp1
            if (i+1<n) and (s[i]=='1' or (s[i]=='2') and s[i+1] in "0123456"):
                # dp[i]+=dp[i+2]
                dp+=dp2
            dp,dp1,dp2=0,dp,dp1
        return dp1
        #TC:O(N)
        #SC:O(1)


        
